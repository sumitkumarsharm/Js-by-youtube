/** Operators in JavaScripts */
/*1. Arithmetic Operator
  (+, -, *, /, %, **, ++, --)
*/
let num1 = 12;
let num2 = 5;
console.log(num1+num2);
console.log(num1-num2);
console.log(num1*num2);
console.log(num1/num2);
console.log(num1%num2);
console.log(num1**num2);
console.log(num1++);
console.log(num1--);


/*2. Assignment Operator
     (=, +=, -=, *=, /=, %= **=)
*/
console.log("/***********this is the Assignment Operator*************\\")
let a = 30;
console.log(a);



let b = 2;
b +=3;
console.log(b)


let c = 5;
c -=4;
console.log(c);


let d = 5;
d *=4;
console.log(d);


let e = 5;
e %=4;
